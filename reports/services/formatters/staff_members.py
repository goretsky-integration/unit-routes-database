def format_birthday_congratulations(
    birthdays: Iterable[StaffMemberBirthday],
) -> str:
    lines = ['<b>–ë–∞–Ω–¥–∞, —Å–µ–≥–æ–¥–Ω—è —Å–≤–æ–π –¥–µ–Ω—å —Ä–æ–∂–¥–µ–Ω–∏—è –ø—Ä–∞–∑–¥–Ω—É—é—Ç:</b>\n']

    for employee_birthday in birthdays:
        name = clean_staff_member_full_name(employee_birthday.full_name)
        unit_name = employee_birthday.unit_name

        lines.append(f'üéÅ {name} –∏–∑ –ø–∏—Ü—Ü–µ—Ä–∏–∏ {unit_name}')

    congratulation_phrase = random.choice(CONGRATULATIONS_PHRASES)

    lines.append(f'\n{congratulation_phrase}')

    return '\n'.join(lines)
